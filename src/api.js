export async function sendMessageToBot(text, currentMessages =[]) {
  try {
    // ===================================================================
    // ТАК БУДЕТ ВЫГЛЯДЕТЬ РЕАЛЬНЫЙ ЗАПРОС К ТВОЕМУ БЭКЕНДУ (FastAPI)
    // ===================================================================
    /*
    const response = await fetch('http://localhost:8000/api/chat', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      // Отправляем текст и историю переписки, чтобы ИИ понимал контекст
      body: JSON.stringify({ message: text, history: currentMessages }),
    });
    
    if (!response.ok) throw new Error('Ошибка сети');
    const data = await response.json();
    
    // Ожидаем, что бэкенд вернет объект вида: 
    // { text: "Ответ ИИ", buttons: ["Кнопка 1", "Кнопка 2"] }
    return data;
    */

    // ===================================================================
    // А ПОКА БЭКЕНД НЕ ГОТОВ — ИСПОЛЬЗУЕМ МОК (ИМИТАЦИЮ)
    // ===================================================================
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve({
          text: `Принято! Я проанализировал твои данные: "${text}". Сейчас я подберу подходящие рестораны и составлю чек-лист.`,
          // Имитируем, что ИИ иногда возвращает кнопки
          buttons: text.includes('Я выбрал дату') ?['Показать рестораны', 'Изменить дату'] :[]
        });
      }, 1500); // Задержка 1.5 секунды (имитация работы GigaChat)
    });

  } catch (error) {
    console.error('Ошибка при отправке сообщения:', error);
    return { text: "Упс, произошла ошибка связи с сервером. Попробуй позже.", buttons:[] };
  }
}